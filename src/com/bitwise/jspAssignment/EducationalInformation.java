package com.bitwise.jspAssignment;

public class EducationalInformation {
private double sscMarks;
private double hscMarks;
private String sscYear;
private String hscYear;
private String schoolName;
private String collegeName;
public double getSscMarks() {
	return sscMarks;
}
public void setSscMarks(double sscMarks) {
	this.sscMarks = sscMarks;
}
public double getHscMarks() {
	return hscMarks;
}
public void setHscMarks(double hscMarks) {
	this.hscMarks = hscMarks;
}
public String getSscYear() {
	return sscYear;
}
public void setSscYear(String sscYear) {
	this.sscYear = sscYear;
}
public String getHscYear() {
	return hscYear;
}
public void setHscYear(String hscYear) {
	this.hscYear = hscYear;
}
public String getSchoolName() {
	return schoolName;
}
public void setSchoolName(String schoolName) {
	this.schoolName = schoolName;
}
public String getCollegeName() {
	return collegeName;
}
public void setCollegeName(String collegeName) {
	this.collegeName = collegeName;
}




}
